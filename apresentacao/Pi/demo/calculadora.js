module.exports = {
    // Função responsável por adicionar 2 números:
    adicionar: (num1, num2) => {
      return num1 + num2;
    },
  
    // Função responsável por subtrair 2 números:
    subtrair: (num1, num2) => {
      return num1 - num2;
    },
  
    // Função responsável por multiplicar 2 números:
    multiplicar: (num1, num2) => {
      return num1 * num2;
    },
  
    // Função responsável por dividir 2 números:
    dividir: (num1, num2) => {
      return num1 / num2;
    },
  };